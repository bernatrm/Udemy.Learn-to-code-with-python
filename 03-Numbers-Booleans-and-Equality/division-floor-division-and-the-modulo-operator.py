# Floor Division: In mathematics and computer science, 
# the floor function is the function that takes as input 
# a real number x, and gives as output the greatest integer less than or equal to x
print(14 // 3) #floor division: integer part of the result
print(-14 // 3) #floor division: rounded integer part of the result

print(14 % 3) #MOD: Divide and get remainder
print(15 % 3)
print(16 % 3)

